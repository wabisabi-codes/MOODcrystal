var skeletal = document.getElementsByClassName("skeletal");
var muscular = document.getElementsByClassName("muscular");
var nervous = document.getElementsByClassName("nervous");
var endocrine = document.getElementsByClassName("endocrine");
var cardiovascular = document.getElementsByClassName("cardiovascular");
var lymphatic = document.getElementsByClassName("lymphatic");
var respiratory = document.getElementsByClassName("respiratory");
var digestive = document.getElementsByClassName("digestive");
var urinary = document.getElementsByClassName("urinary");
var reproductive = document.getElementsByClassName("reproductive");

//SHOW CRYSTALS FOR SKELETAL SYSTEMS//
function loadSkeletal() {
  for (var i = 0; i < skeletal.length; i++) {
    if (skeletal[i].style.display === "none") {
      skeletal[i].style.display = "block";
    } else {
      skeletal[i].style.display = "none";
    }
  }
}

