//SHOW CRSYTALS FOR ANGRY//
function loadAngry() {
  for (var i = 0; i < angry.length; i++) {
    if (angry[i].style.display === "none") {
      angry[i].style.display = "block";
    } else {
      angry[i].style.display = "none";
    }
  }
}

